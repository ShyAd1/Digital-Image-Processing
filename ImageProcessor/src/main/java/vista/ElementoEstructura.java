    public int[][] obtenerElementoEstructura() {
        int[][] estructura = new int[3][3];

        try { estructura[0][0] = Integer.parseInt(jTextField1.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[0][1] = Integer.parseInt(jTextField2.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[0][2] = Integer.parseInt(jTextField3.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[1][0] = Integer.parseInt(jTextField6.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[1][1] = Integer.parseInt(jTextField5.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[1][2] = Integer.parseInt(jTextField4.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[2][0] = Integer.parseInt(jTextField9.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[2][1] = Integer.parseInt(jTextField8.getText().trim()); } catch (NumberFormatException e) {}
        try { estructura[2][2] = Integer.parseInt(jTextField7.getText().trim()); } catch (NumberFormatException e) {}

        return estructura;
    }


